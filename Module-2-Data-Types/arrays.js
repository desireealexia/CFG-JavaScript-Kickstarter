// arrays

let fruits = ["apple", "plum", "cherry"];

console.log(fruits);

console.log(fruits[0]);
console.log(fruits[1]);
console.log(fruits[2]);

// replace

fruits[0] = "banana";

console.log(fruits);

// add
fruits.push("orange", "grape", "kiwi");

console.log(fruits);

// remove

fruits.pop();

console.log(fruits);

// length of an array

console.log(fruits.length);
