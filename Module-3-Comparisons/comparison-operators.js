// ==

console.log(5 == 5);
console.log(5 == "5");

// ===

console.log(5 === 5);
console.log(5 === "5");

// !=

console.log(5 != 5);
console.log(5 != 6);
console.log(5 != "5");

// !==

console.log(5 !== 5);
console.log(5 !== 6);
console.log(5 !== "5");

// > >=

console.log(10 > 6);
console.log(10 > 10);
console.log(10 >= 10);

// < <=

console.log(10 < 6);
console.log(10 < 10);
console.log(10 <= 10);

